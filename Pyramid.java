class Pyramid
{
    public static void main(String args[])
    {
        int i, j;

        System.out.println("KEVIN A DOBBIN MCA 'A' SEC\n");

        for (i = 1; i <= 4; i++)
        {
            for (j = 1; j <= i; j++)
            {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
